    private void signInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInButtonActionPerformed
        String username = usernameText.getText().trim();
        String password = new String(passwordText.getPassword());
        try {
            Users user = (Users) em.createNamedQuery("Users.findByUserName")
                    .setParameter("userName", username)
                    .getSingleResult();
            if (user.getPassword().equals(password)) {
                JFrame nextFrame = null;
                switch ((char) user.getImpRole()) {
                    case 'U':// department user
                        nextFrame = new DeptUI((int) user.getDeptId().getDeptId(), (int)user.getUserId());
                        break;
                    case 'P':// purchasing manager
                        nextFrame = new PurchasingManager();
                        break;
                    case 'S':// storage manager
                        nextFrame = new StoreManager();
                        break;
                }
                nextFrame.addWindowListener(new WindowAdapter() {
                    
                    @Override
                    public void windowClosed(WindowEvent e) {
                        MainMenu.this.setVisible(true);
                    }
                    
                });
                nextFrame.setLocationRelativeTo(this);
                nextFrame.setVisible(true);
                this.setVisible(false);
            } else {
                TooltipManager.showOneTimeTooltip(passwordText, null, "<html><center><font size = 4><font color = red>Incorrcet password</font>"
                        + "<br> Please retry</font></center></html>", TooltipWay.trailing);
            }
        } catch (NoResultException e) {
                TooltipManager.showOneTimeTooltip(usernameText, null, "<html><center><font size = 4><font color = red>User does not exist</font>"
                        + "<br>Please check spelling and retry</font></center></html>", TooltipWay.trailing);
        }
    }//GEN-LAST:event_signInButtonActionPerformed

